def getColormap(ftype):
  if(ftype in colormaps.keys()):
    return colormaps[ftype]
  else:
    return colormaps['default']

def getColor(value, ftype=None):
  colormap = getColormap(ftype)
  for i in colormap:
    if(value <= i[1]):
      return i[2]
  return colormap[len(colormap) - 1][2]



